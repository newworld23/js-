/* switch syntax:
switch (key) {
    case value:
        
        break;

    default:
        break;
}
*/


const month = 3;

switch (month) {
    case 1:
        console.log("January");
        break;
    case 2:
        console.log("Feb");
        break;
    case 3:
        console.log("March");
        break;
    case 4:
        console.log("Aptil");
        break;

    default:
        console.log("not match");
        break;
}